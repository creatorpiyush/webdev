var a = prompt("enter month (jan- dec) :- ");

switch (a) {
    case 'jan':
        alert("1st month of year ");
        break;

    case 'feb':
        alert("2st month of year ");
        break;

    case 'mar':
        alert('3st month of year ');
        break;

    case 'apr':
        alert("4st month of year ");
        break;

    case 'may':
        alert("5st month of year ");
        break;

    default:
        break;
}